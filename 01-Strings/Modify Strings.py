

# Upper Case
a = "Hey You, Bitch!"
print(a.upper())


# Lower Case
b = "Hey You, Bitch!"
print(b.lower())


# Remove Whitespace
c = "Hey You, Bitch! "
print(c.strip())


# Replace Whitespace
d = "Hey You, Bitch"
print(d.replace("Hey", "Hi"))


# Split String
e = "Hello, World!"
f = e.split(",")
print(f)